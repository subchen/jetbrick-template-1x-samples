package jetbrick.template.samples.model;

public class UserInfo {
    protected Integer id; // ID
    protected String name; // 姓名(name)
    protected String email; // 电子邮件(email)
    protected Integer status; // 用户状态(status)

    public UserInfo() {
    }

    public UserInfo(Integer id, String name, String email, Integer status) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.status = status;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }
}
